package org.levpro.pharmaffiliates.models;

public class Childrens {
    private String url;
    private String name;

    public Childrens(String url, String name) {
        this.url = url;
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public String getName() {
        return name;
    }
}
